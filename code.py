def build_fixed_topk_detail_summary(
    topk_prediction_detail: pd.DataFrame,
    fixed_k_values: list[int]
) -> pd.DataFrame:
    summary_tables = []

    grouping_columns = [
        "experiment_key",
        "network",
        "fold_id",
        "method",
        "score_column",
    ]

    for k in fixed_k_values:
        subset = topk_prediction_detail[
            topk_prediction_detail["rank"] <= k
        ].copy()

        summary = (
            subset
            .groupby(grouping_columns)
            .agg(
                effective_k=("rank", "count"),
                true_positives=("label", "sum"),
                mean_score=("score", "mean"),
                min_rank=("rank", "min"),
                max_rank=("rank", "max"),
                mean_source_degree_train=("source_degree_train", "mean"),
                mean_target_degree_train=("target_degree_train", "mean"),
                mean_degree_product_train=("degree_product_train", "mean"),
                mean_common_neighbors_train=("common_neighbors_train", "mean"),
            )
            .reset_index()
        )

        summary["k_type"] = "fixed"
        summary["k_label"] = f"at_{k}"
        summary["k_value"] = int(k)

        summary["false_positives"] = (
            summary["effective_k"] - summary["true_positives"]
        )

        summary["precision_from_detail"] = summary.apply(
            lambda row: safe_divide(
                row["true_positives"],
                row["effective_k"]
            ),
            axis=1
        )

        summary_tables.append(summary)

    fixed_summary = pd.concat(summary_tables, ignore_index=True)

    ordered_columns = [
        "experiment_key",
        "network",
        "fold_id",
        "method",
        "score_column",
        "k_type",
        "k_label",
        "k_value",
        "effective_k",
        "true_positives",
        "false_positives",
        "precision_from_detail",
        "mean_score",
        "min_rank",
        "max_rank",
        "mean_source_degree_train",
        "mean_target_degree_train",
        "mean_degree_product_train",
        "mean_common_neighbors_train",
    ]

    return fixed_summary[ordered_columns]


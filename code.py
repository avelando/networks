def validate_topk_error_analysis_outputs(
    topk_error_analysis_dir: Path,
    required_paths: list[Path],
    required_dataframes: dict[str, pd.DataFrame],
    topk_prediction_detail: pd.DataFrame,
    topk_confusion_summary: pd.DataFrame,
    topk_fixed_k_detail_summary: pd.DataFrame,
    evaluation_results: pd.DataFrame,
    method_score_columns: dict[str, str],
    fixed_k_values: list[int],
    max_k: int
) -> None:
    assert topk_error_analysis_dir.exists(), (
        f"Top-K error analysis directory not found: {topk_error_analysis_dir}"
    )

    for path in required_paths:
        assert path.exists(), f"Expected top-K analysis output file not found: {path}"

    for dataframe_name, dataframe in required_dataframes.items():
        assert isinstance(dataframe, pd.DataFrame), (
            f"{dataframe_name} is not a pandas DataFrame."
        )

        assert len(dataframe) > 0, (
            f"{dataframe_name} is empty."
        )

    assert topk_prediction_detail["rank"].min() >= 1, (
        "Top-K detail contains ranks smaller than 1."
    )

    assert topk_prediction_detail["rank"].max() <= max_k, (
        f"Top-K detail contains ranks greater than {max_k}."
    )

    assert set(topk_prediction_detail["topk_outcome"].unique()).issubset(
        {"true_positive", "false_positive"}
    ), (
        "Unexpected top-K outcome labels were found."
    )

    expected_method_count = len(method_score_columns)

    methods_in_detail = topk_prediction_detail["method"].nunique()

    assert methods_in_detail == expected_method_count, (
        f"Expected {expected_method_count} methods in top-K detail, "
        f"got {methods_in_detail}."
    )

    fixed_confusion = topk_confusion_summary[
        topk_confusion_summary["k_type"] == "fixed"
    ].copy()

    fixed_detail = topk_fixed_k_detail_summary.copy()

    comparison_columns = [
        "experiment_key",
        "network",
        "fold_id",
        "method",
        "score_column",
        "k_value",
    ]

    comparison = fixed_detail.merge(
        fixed_confusion[
            comparison_columns + ["tp", "fp", "effective_k"]
        ],
        on=comparison_columns,
        how="left"
    )

    assert comparison["tp"].notna().all(), (
        "Some fixed top-K detail rows were not found in the confusion summary."
    )

    assert (
        comparison["true_positives"].astype(int)
        == comparison["tp"].astype(int)
    ).all(), (
        "True positive counts in top-K detail do not match evaluation_results."
    )

    assert (
        comparison["false_positives"].astype(int)
        == comparison["fp"].astype(int)
    ).all(), (
        "False positive counts in top-K detail do not match evaluation_results."
    )

    assert (
        comparison["effective_k_x"].astype(int)
        == comparison["effective_k_y"].astype(int)
    ).all(), (
        "Effective K values in top-K detail do not match evaluation_results."
    )

    expected_fixed_rows = (
        len(evaluation_results)
        * len(fixed_k_values)
    )

    actual_fixed_rows = len(fixed_detail)

    assert actual_fixed_rows == expected_fixed_rows, (
        f"Expected {expected_fixed_rows} fixed top-K summary rows, "
        f"got {actual_fixed_rows}."
    )

    manifest_paths = output_manifest["path"].apply(Path)

    missing_manifest_paths = [
        path for path in manifest_paths
        if not path.exists()
    ]

    assert len(missing_manifest_paths) == 0, (
        f"Some files listed in output_manifest do not exist: {missing_manifest_paths}"
    )

    print("Top-K error/hit analysis outputs were successfully validated.")



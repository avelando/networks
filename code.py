"""## **2. Experimental Protocol for Link Prediction with Cross-Validation**

After downloading, loading, and preprocessing the networks, this step defines the experimental protocol used to evaluate classical link prediction methods.

The problem is formulated as follows: for each network, a portion of its edges is removed and used as the positive test set. Subsequently, link prediction methods receive only the training graph and must assign scores to candidate node pairs. Pairs with higher scores are interpreted as more probable connections.

To approximate the experimental protocol of the base paper, the evaluation is performed using five-fold cross-validation. In each fold, a subset of edges is removed from the original network and used as the positive test set. The remaining graph is used as the training graph.

To prevent the removal of edges from excessively fragmenting the networks, the procedure preserves a spanning tree of the network. The edges of this tree are mandatorily kept in the training set, and only the other edges can be distributed among the test folds. This ensures that the training graph remains connected across all folds.
"""


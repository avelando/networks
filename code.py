def build_topk_prediction_detail(
    scored_candidate_tables: dict[str, pd.DataFrame],
    method_score_columns: dict[str, str],
    max_k: int
) -> pd.DataFrame:
    detail_rows = []

    total_iterations = len(scored_candidate_tables) * len(method_score_columns)

    iterator = tqdm(
        [
            (experiment_key, method_name, score_column)
            for experiment_key in scored_candidate_tables.keys()
            for method_name, score_column in method_score_columns.items()
        ],
        total=total_iterations,
        desc="Extracting top-K predictions"
    )

    for experiment_key, method_name, score_column in iterator:
        scored_table = scored_candidate_tables[experiment_key]

        ranked_table = scored_table.sort_values(
            by=score_column,
            ascending=False,
            kind="mergesort"
        ).head(max_k).copy()

        ranked_table = ranked_table[
            [
                "network",
                "fold_id",
                "source",
                "target",
                "label",
                score_column,
            ]
        ].rename(columns={score_column: "score"})

        ranked_table["experiment_key"] = experiment_key
        ranked_table["method"] = method_name
        ranked_table["score_column"] = score_column
        ranked_table["rank"] = np.arange(1, len(ranked_table) + 1)

        ranked_table["topk_outcome"] = np.where(
            ranked_table["label"].astype(int) == 1,
            "true_positive",
            "false_positive"
        )

        detail_rows.append(ranked_table)

    topk_detail = pd.concat(detail_rows, ignore_index=True)

    ordered_columns = [
        "experiment_key",
        "network",
        "fold_id",
        "method",
        "score_column",
        "rank",
        "source",
        "target",
        "label",
        "topk_outcome",
        "score",
    ]

    return topk_detail[ordered_columns]


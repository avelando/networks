def create_final_results_zip(
    zip_path: Path,
    zip_manifest: pd.DataFrame,
    additional_files: list[Path]
) -> None:
    zip_path.parent.mkdir(parents=True, exist_ok=True)

    files_to_zip = []

    for _, row in zip_manifest.iterrows():
        files_to_zip.append(
            (
                Path(row["absolute_path"]),
                row["relative_path"]
            )
        )

    for additional_file in additional_files:
        additional_file = Path(additional_file)

        if additional_file.exists():
            files_to_zip.append(
                (
                    additional_file,
                    get_relative_path_for_export(additional_file)
                )
            )

    seen_archive_names = set()
    unique_files_to_zip = []

    for file_path, archive_name in files_to_zip:
        if archive_name in seen_archive_names:
            continue

        seen_archive_names.add(archive_name)
        unique_files_to_zip.append((file_path, archive_name))

    with zipfile.ZipFile(
        zip_path,
        mode="w",
        compression=zipfile.ZIP_DEFLATED,
        compresslevel=6
    ) as zip_file:
        for file_path, archive_name in tqdm(
            unique_files_to_zip,
            desc="Creating final ZIP"
        ):
            assert file_path.exists(), f"File not found during ZIP creation: {file_path}"

            zip_file.write(
                filename=file_path,
                arcname=archive_name
            )

def validate_final_results_zip(
    zip_path: Path,
    zip_manifest: pd.DataFrame,
    additional_files: list[Path]
) -> None:
    assert zip_path.exists(), f"ZIP file was not created: {zip_path}"
    assert zip_path.stat().st_size > 0, f"ZIP file is empty: {zip_path}"

    expected_archive_names = set(zip_manifest["relative_path"])

    for additional_file in additional_files:
        additional_file = Path(additional_file)

        if additional_file.exists():
            expected_archive_names.add(
                get_relative_path_for_export(additional_file)
            )

    with zipfile.ZipFile(zip_path, "r") as zip_file:
        zipped_archive_names = set(zip_file.namelist())

        corrupted_file = zip_file.testzip()

    assert corrupted_file is None, (
        f"The ZIP file contains a corrupted entry: {corrupted_file}"
    )

    missing_from_zip = expected_archive_names - zipped_archive_names

    assert len(missing_from_zip) == 0, (
        f"Some expected files were not included in the ZIP: "
        f"{sorted(missing_from_zip)[:30]}"
    )

    print("Final ZIP file was successfully validated.")


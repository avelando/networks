def build_network_best_worst_method_table(
    cross_validation_results: pd.DataFrame,
    primary_metric: str
) -> pd.DataFrame:
    rows = []

    for network, network_data in cross_validation_results.groupby("network"):
        ranked_data = network_data.sort_values(
            by=primary_metric,
            ascending=False,
            kind="mergesort"
        ).reset_index(drop=True)

        best_row = ranked_data.iloc[0]
        worst_row = ranked_data.iloc[-1]

        if len(ranked_data) > 1:
            second_row = ranked_data.iloc[1]
            second_method = second_row["method"]
            second_score = float(second_row[primary_metric])
        else:
            second_method = np.nan
            second_score = np.nan

        best_score = float(best_row[primary_metric])
        worst_score = float(worst_row[primary_metric])

        rows.append({
            "network": network,
            "best_method": best_row["method"],
            "best_method_family": assign_method_family(best_row["method"]),
            "best_score_column": best_row["score_column"],
            "best_primary_metric": best_score,
            "second_best_method": second_method,
            "second_best_primary_metric": second_score,
            "worst_method": worst_row["method"],
            "worst_method_family": assign_method_family(worst_row["method"]),
            "worst_score_column": worst_row["score_column"],
            "worst_primary_metric": worst_score,
            "top1_top2_gap": (
                best_score - second_score
                if pd.notna(second_score)
                else np.nan
            ),
            "best_worst_gap": best_score - worst_score,
        })

    return pd.DataFrame(rows)


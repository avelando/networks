def build_fold_rank_stability_table(
    fold_rank_table: pd.DataFrame,
    metric: str
) -> pd.DataFrame:
    rank_column = f"rank_by_{metric}_within_fold"

    rank_stability = (
        fold_rank_table
        .groupby(["network", "method", "score_column"])
        .agg(
            fold_count=("fold_id", "nunique"),
            mean_rank=(rank_column, "mean"),
            std_rank=(rank_column, "std"),
            best_rank=(rank_column, "min"),
            worst_rank=(rank_column, "max"),
            mean_metric=(metric, "mean"),
            std_metric=(metric, "std"),
            min_metric=(metric, "min"),
            max_metric=(metric, "max"),
        )
        .reset_index()
    )

    rank_stability["std_rank"] = rank_stability["std_rank"].fillna(0.0)
    rank_stability["std_metric"] = rank_stability["std_metric"].fillna(0.0)

    rank_stability["rank_range"] = (
        rank_stability["worst_rank"] - rank_stability["best_rank"]
    )

    rank_stability["metric_range"] = (
        rank_stability["max_metric"] - rank_stability["min_metric"]
    )

    rank_stability["metric_cv"] = rank_stability.apply(
        lambda row: safe_coefficient_of_variation(
            mean_value=row["mean_metric"],
            std_value=row["std_metric"]
        ),
        axis=1
    )

    rank_stability = rank_stability.sort_values(
        by=["network", "mean_rank", "std_rank", "rank_range", "metric_cv"],
        ascending=[True, True, True, True, True],
        kind="mergesort"
    ).reset_index(drop=True)

    return rank_stability


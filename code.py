def build_topk_outcome_summary(
    topk_confusion_summary: pd.DataFrame
) -> pd.DataFrame:
    aggregation_columns = [
        "network",
        "method",
        "score_column",
        "k_type",
        "k_label",
        "k_value",
    ]

    summary = (
        topk_confusion_summary
        .groupby(aggregation_columns)
        .agg(
            fold_count=("fold_id", "nunique"),
            mean_effective_k=("effective_k", "mean"),
            mean_tp=("tp", "mean"),
            std_tp=("tp", "std"),
            mean_fp=("fp", "mean"),
            std_fp=("fp", "std"),
            mean_fn=("fn", "mean"),
            std_fn=("fn", "std"),
            mean_tn=("tn", "mean"),
            std_tn=("tn", "std"),
            mean_precision=("precision", "mean"),
            std_precision=("precision", "std"),
            mean_recall=("recall", "mean"),
            std_recall=("recall", "std"),
            mean_f1=("f1", "mean"),
            std_f1=("f1", "std"),
            mean_ndcg=("ndcg", "mean"),
            std_ndcg=("ndcg", "std"),
            mean_mrr=("mrr", "mean"),
            std_mrr=("mrr", "std"),
            mean_lift=("lift", "mean"),
            std_lift=("lift", "std"),
        )
        .reset_index()
    )

    std_columns = [
        column for column in summary.columns
        if column.startswith("std_")
    ]

    summary[std_columns] = summary[std_columns].fillna(0.0)

    summary["mean_error_rate_in_topk"] = summary.apply(
        lambda row: safe_divide(
            row["mean_fp"],
            row["mean_tp"] + row["mean_fp"]
        ),
        axis=1
    )

    summary = summary.sort_values(
        by=[
            "network",
            "k_type",
            "k_value",
            "mean_precision",
            "mean_recall",
            "mean_fp",
        ],
        ascending=[True, True, True, False, False, True],
        kind="mergesort"
    ).reset_index(drop=True)

    return summary


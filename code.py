def safe_coefficient_of_variation(mean_value: float, std_value: float) -> float:
    if pd.isna(mean_value) or pd.isna(std_value):
        return np.nan

    if mean_value == 0:
        return np.nan

    return float(std_value / abs(mean_value))

def build_fold_metric_robustness_table(
    evaluation_results: pd.DataFrame,
    metrics: list[str]
) -> pd.DataFrame:
    robustness_rows = []

    grouping_columns = ["network", "method", "score_column"]

    for (network, method, score_column), group in evaluation_results.groupby(grouping_columns):
        row = {
            "network": network,
            "method": method,
            "score_column": score_column,
            "fold_count": group["fold_id"].nunique(),
        }

        for metric in metrics:
            values = group[metric].dropna()

            if len(values) == 0:
                metric_mean = np.nan
                metric_std = np.nan
                metric_min = np.nan
                metric_max = np.nan
                metric_range = np.nan
                metric_cv = np.nan
            else:
                metric_mean = float(values.mean())
                metric_std = float(values.std(ddof=1)) if len(values) > 1 else 0.0
                metric_min = float(values.min())
                metric_max = float(values.max())
                metric_range = float(metric_max - metric_min)
                metric_cv = safe_coefficient_of_variation(metric_mean, metric_std)

            row[f"{metric}_mean"] = metric_mean
            row[f"{metric}_std"] = metric_std
            row[f"{metric}_min"] = metric_min
            row[f"{metric}_max"] = metric_max
            row[f"{metric}_range"] = metric_range
            row[f"{metric}_cv"] = metric_cv

        robustness_rows.append(row)

    robustness_table = pd.DataFrame(robustness_rows)

    return robustness_table

fold_metric_robustness_table = build_fold_metric_robustness_table(
    evaluation_results=evaluation_results,
    metrics=FOLD_ROBUSTNESS_METRICS
)


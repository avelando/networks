def normalize_edge(edge: tuple) -> tuple:
    source, target = edge
    return tuple(sorted((source, target)))

def get_normalized_edge_set(graph: nx.Graph) -> set[tuple]:
    return {normalize_edge(edge) for edge in graph.edges()}

def build_connected_edge_folds(
    graph: nx.Graph,
    n_folds: int = 5,
    random_state: int = 42
) -> list[list[tuple]]:
    if graph.is_directed():
        raise ValueError("The graph must be undirected.")

    if not nx.is_connected(graph):
        raise ValueError("The graph must be connected before splitting.")

    rng = random.Random(random_state)

    all_edges = [normalize_edge(edge) for edge in graph.edges()]

    spanning_tree = nx.minimum_spanning_tree(graph)
    spanning_tree_edges = get_normalized_edge_set(spanning_tree)

    removable_edges = [
        edge for edge in all_edges
        if edge not in spanning_tree_edges
    ]

    if len(removable_edges) < n_folds:
        raise ValueError(
            f"Not enough removable edges for {n_folds} folds. "
            f"Available removable edges: {len(removable_edges)}."
        )

    rng.shuffle(removable_edges)

    fold_arrays = np.array_split(removable_edges, n_folds)

    edge_folds = [
        [tuple(edge) for edge in fold_array.tolist()]
        for fold_array in fold_arrays
    ]

    return edge_folds

def parse_experiment_key(experiment_key: str) -> tuple[str, int]:
    network_name, fold_part = experiment_key.rsplit("__fold_", 1)
    return network_name, int(fold_part)

def sample_negative_edges(
    graph: nx.Graph,
    number_of_samples: int,
    excluded_edges: set[tuple] | None = None,
    random_state: int = 42
) -> list[tuple]:
    rng = random.Random(random_state)
    nodes = list(graph.nodes())
    existing_edges = get_normalized_edge_set(graph)

    if excluded_edges is None:
        excluded_edges = set()

    negative_edges = set()
    max_attempts = number_of_samples * 100
    attempts = 0

    while len(negative_edges) < number_of_samples and attempts < max_attempts:
        source, target = rng.sample(nodes, 2)
        edge = normalize_edge((source, target))

        if edge not in existing_edges and edge not in excluded_edges:
            negative_edges.add(edge)

        attempts += 1

    if len(negative_edges) < number_of_samples:
        raise RuntimeError(
            f"Could only sample {len(negative_edges)} negative edges "
            f"out of {number_of_samples} requested."
        )

    return list(negative_edges)


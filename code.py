def validate_final_reproducibility_outputs(
    required_paths: list[Path],
    required_dataframes: dict[str, pd.DataFrame],
    output_manifest: pd.DataFrame,
    pipeline_integrity_checks: pd.DataFrame,
    expected_stages: set[str]
) -> None:
    for path in required_paths:
        assert path.exists(), f"Expected reproducibility output not found: {path}"
        assert path.stat().st_size > 0, f"Output file is empty: {path}"

    for dataframe_name, dataframe in required_dataframes.items():
        assert isinstance(dataframe, pd.DataFrame), (
            f"{dataframe_name} is not a pandas DataFrame."
        )

        assert len(dataframe) > 0, (
            f"{dataframe_name} is empty."
        )

    assert pipeline_integrity_checks["passed"].all(), (
        "At least one pipeline integrity check failed."
    )

    manifest_stages = set(output_manifest["stage"].astype(str))
    missing_stages = expected_stages - manifest_stages

    assert len(missing_stages) == 0, (
        f"Some expected stages are missing from output_manifest: {missing_stages}"
    )

    manifest_paths = output_manifest["path"].apply(Path)

    missing_manifest_paths = [
        path for path in manifest_paths
        if not path.exists()
    ]

    assert len(missing_manifest_paths) == 0, (
        f"Some files listed in output_manifest do not exist: {missing_manifest_paths}"
    )

    print("Final reproducibility outputs were successfully validated.")


def sanitize_filename(value: str) -> str:
    value = str(value)
    value = value.replace(" ", "_")
    value = value.replace("/", "_")
    value = value.replace("\\", "_")
    value = value.replace(":", "_")
    value = value.replace(";", "_")
    value = value.replace(",", "_")
    value = value.replace("(", "")
    value = value.replace(")", "")
    value = value.replace("[", "")
    value = value.replace("]", "")
    value = value.replace("{", "")
    value = value.replace("}", "")
    value = value.replace("=", "_")
    value = value.replace("+", "plus")
    value = value.replace("-", "_")
    value = value.lower()

    while "__" in value:
        value = value.replace("__", "_")

    return value.strip("_")

def save_current_figure(
    output_dir: Path,
    figure_name: str,
    description: str,
    stage: str = "10",
    dpi: int = 300
) -> list[dict]:
    output_dir.mkdir(parents=True, exist_ok=True)


def build_method_network_heatmap_data(
    cross_validation_results: pd.DataFrame,
    metric: str,
    top_n_methods: int = 20
) -> pd.DataFrame:
    pivot_table = cross_validation_results.pivot(
        index="method",
        columns="network",
        values=metric
    )

    ordered_methods = (
        pivot_table
        .mean(axis=1)
        .sort_values(ascending=False)
        .head(top_n_methods)
        .index
    )

    return pivot_table.loc[ordered_methods]

def build_family_network_heatmap_data(
    family_performance_by_network: pd.DataFrame,
    metric: str = "mean_average_precision"
) -> pd.DataFrame:
    pivot_table = family_performance_by_network.pivot(
        index="method_family",
        columns="network",
        values=metric
    )

    ordered_families = (
        pivot_table
        .mean(axis=1)
        .sort_values(ascending=False)
        .index
    )

    return pivot_table.loc[ordered_families]


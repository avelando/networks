def build_most_robust_methods_by_network(
    fold_metric_robustness_table: pd.DataFrame,
    fold_rank_stability_table: pd.DataFrame,
    primary_metric: str,
    top_n: int = 10
) -> pd.DataFrame:
    metric_mean_column = f"{primary_metric}_mean"
    metric_std_column = f"{primary_metric}_std"
    metric_range_column = f"{primary_metric}_range"
    metric_cv_column = f"{primary_metric}_cv"

    required_columns = {
        "network",
        "method",
        "score_column",
        metric_mean_column,
        metric_std_column,
        metric_range_column,
        metric_cv_column,
    }

    missing_columns = required_columns - set(fold_metric_robustness_table.columns)

    assert len(missing_columns) == 0, (
        f"Missing robustness columns: {missing_columns}"
    )

    rank_columns = [
        "network",
        "method",
        "score_column",
        "mean_rank",
        "std_rank",
        "best_rank",
        "worst_rank",
        "rank_range",
    ]

    merged_table = fold_metric_robustness_table.merge(
        fold_rank_stability_table[rank_columns],
        on=["network", "method", "score_column"],
        how="left"
    )

    robust_rows = []

    for network, network_data in merged_table.groupby("network"):
        ranked_network_data = network_data.sort_values(
            by=[
                metric_mean_column,
                metric_std_column,
                metric_range_column,
                "std_rank",
                "rank_range",
            ],
            ascending=[False, True, True, True, True],
            kind="mergesort"
        ).head(top_n).copy()

        ranked_network_data["robustness_rank"] = np.arange(
            1,
            len(ranked_network_data) + 1
        )

        robust_rows.append(ranked_network_data)

    robust_methods = pd.concat(robust_rows, ignore_index=True)

    ordered_columns = [
        "network",
        "robustness_rank",
        "method",
        "score_column",
        "fold_count",
        metric_mean_column,
        metric_std_column,
        metric_range_column,
        metric_cv_column,
        "mean_rank",
        "std_rank",
        "best_rank",
        "worst_rank",
        "rank_range",
    ]

    available_columns = [
        column for column in ordered_columns
        if column in robust_methods.columns
    ]

    return robust_methods[available_columns]


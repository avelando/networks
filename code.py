def build_topological_cache(graph: nx.Graph) -> dict:
    neighbors = {
        node: set(graph.neighbors(node))
        for node in graph.nodes()
    }

    degrees = {
        node: graph.degree(node)
        for node in graph.nodes()
    }

    node_to_index, index_to_node = build_node_index(graph)

    degrees_array = np.array(
        [degrees[index_to_node[index]] for index in range(graph.number_of_nodes())],
        dtype=float
    )

    adjacency_matrix = build_sparse_adjacency_matrix(
        graph=graph,
        node_to_index=node_to_index
    )

    transition_matrix = build_transition_matrix(
        adjacency_matrix=adjacency_matrix,
        degrees_array=degrees_array
    )

    flow_matrix = build_symmetric_flow_matrix(
        adjacency_matrix=adjacency_matrix,
        degrees_array=degrees_array
    )

    number_of_nodes = graph.number_of_nodes()
    number_of_edges = graph.number_of_edges()

    possible_edges = number_of_nodes * (number_of_nodes - 1) / 2
    non_edges = possible_edges - number_of_edges

    average_degree = (
        2 * number_of_edges / number_of_nodes
        if number_of_nodes > 0
        else 0.0
    )

    prior_odds = safe_divide(non_edges, number_of_edges)

    local_role = {}

    for node in graph.nodes():
        degree = degrees[node]
        node_neighbors = neighbors[node]

        possible_neighbor_edges = degree * (degree - 1) / 2
        connected_neighbor_edges = count_edges_inside_node_set(
            node_set=node_neighbors,
            neighbors=neighbors
        )
        unconnected_neighbor_edges = possible_neighbor_edges - connected_neighbor_edges

        local_role[node] = safe_divide(
            2 * connected_neighbor_edges + 1,
            2 * unconnected_neighbor_edges + 1
        )

    return {
        "neighbors": neighbors,
        "degrees": degrees,
        "node_to_index": node_to_index,
        "index_to_node": index_to_node,
        "degrees_array": degrees_array,
        "adjacency_matrix": adjacency_matrix,
        "transition_matrix": transition_matrix,
        "flow_matrix": flow_matrix,
        "number_of_nodes": number_of_nodes,
        "number_of_edges": number_of_edges,
        "average_degree": average_degree,
        "prior_odds": prior_odds,
        "local_role": local_role,
    }

def compute_ra_cni_score(
    source_neighbors: set,
    target_neighbors: set,
    neighbors: dict,
    degrees: dict,
    base_resource_allocation_score: float
) -> float:
    interaction_score = 0.0

    for node_i in source_neighbors:
        for node_j in neighbors[node_i].intersection(target_neighbors):
            if node_i == node_j:
                continue

            degree_i = degrees[node_i]
            degree_j = degrees[node_j]

            if degree_i == degree_j:
                continue

            lower_degree = min(degree_i, degree_j)
            higher_degree = max(degree_i, degree_j)

            interaction_score += safe_divide(1.0, lower_degree) - safe_divide(1.0, higher_degree)

    return base_resource_allocation_score + interaction_score

def compute_fsw_score(
    source_neighbors: set,
    target_neighbors: set,
    common_neighbors_count: int,
    average_degree: float
) -> float:
    exclusive_count = len(source_neighbors.symmetric_difference(target_neighbors))

    lambda_value = max(
        0.0,
        average_degree - (exclusive_count + common_neighbors_count)
    )

    denominator = exclusive_count + 2 * common_neighbors_count + lambda_value

    return safe_divide(
        2 * common_neighbors_count,
        denominator
    ) ** 2

def compute_self_information_link(
    source,
    target,
    degrees: dict,
    number_of_edges: int
) -> float:
    source_degree = degrees[source]
    target_degree = degrees[target]

    if number_of_edges <= 0:
        return 0.0

    product_term = 1.0

    for i in range(1, target_degree + 1):
        numerator = number_of_edges - source_degree - i + 1
        denominator = number_of_edges - i + 1

        if denominator <= 0:
            continue

        product_term *= max(0.0, numerator / denominator)

    probability = 1.0 - product_term
    probability = min(max(probability, 1e-15), 1.0)

    return -math.log2(probability)

def compute_conditional_self_information_given_node(
    source,
    target,
    conditioning_node,
    neighbors: dict
) -> float:
    conditioning_neighbors = neighbors[conditioning_node]
    degree = len(conditioning_neighbors)

    if degree < 2:
        return 0.0

    possible_pairs = degree * (degree - 1) / 2
    connected_pairs = count_edges_inside_node_set(
        node_set=conditioning_neighbors,
        neighbors=neighbors
    )

    probability = safe_divide(connected_pairs, possible_pairs)
    probability = min(max(probability, 1e-15), 1.0)

    return -math.log2(probability)

def compute_mutual_information_score(
    source,
    target,
    common_neighbors: set,
    neighbors: dict,
    degrees: dict,
    number_of_edges: int
) -> float:
    base_information = compute_self_information_link(
        source=source,
        target=target,
        degrees=degrees,
        number_of_edges=number_of_edges
    )

    score = -base_information

    for common_neighbor in common_neighbors:
        common_neighbor_neighbors = list(neighbors[common_neighbor])
        degree = degrees[common_neighbor]

        if degree < 2:
            continue

        pair_count = degree * (degree - 1)

        mutual_information_sum = 0.0

        for node_u in common_neighbor_neighbors:
            for node_v in common_neighbor_neighbors:
                if node_u == node_v:
                    continue

                information_uv = compute_self_information_link(
                    source=node_u,
                    target=node_v,
                    degrees=degrees,
                    number_of_edges=number_of_edges
                )

                conditional_information_uv = compute_conditional_self_information_given_node(
                    source=node_u,
                    target=node_v,
                    conditioning_node=common_neighbor,
                    neighbors=neighbors
                )

                mutual_information_sum += information_uv - conditional_information_uv

        score += safe_divide(mutual_information_sum, pair_count)

    return float(score)

def compute_friendlink_score(
    number_of_nodes: int,
    path_counts_by_length: dict[int, int],
    maximum_length: int
) -> float:
    score = 0.0

    for path_length in range(2, maximum_length + 1):
        path_count = path_counts_by_length.get(path_length, 0)

        denominator = 1.0
        for value in range(2, path_length + 1):
            denominator *= number_of_nodes - value

        if denominator <= 0:
            continue

        score += safe_divide(
            path_count,
            (path_length - 1) * denominator
        )

    return float(score)

def count_walks_by_length_between_pair(
    source,
    target,
    neighbors: dict,
    maximum_length: int
) -> dict[int, int]:
    current_counts = {source: 1}
    path_counts_by_length = {}

    for path_length in range(1, maximum_length + 1):
        next_counts = defaultdict(int)

        for node, count in current_counts.items():
            for neighbor in neighbors[node]:
                next_counts[neighbor] += count

        current_counts = dict(next_counts)
        path_counts_by_length[path_length] = current_counts.get(target, 0)

    return path_counts_by_length

def compute_lpi_score(
    path_counts_by_length: dict[int, int],
    beta: float,
    maximum_length: int
) -> float:
    score = 0.0

    for path_length in range(2, maximum_length + 1):
        score += (beta ** (path_length - 2)) * path_counts_by_length.get(path_length, 0)

    return float(score)

def compute_ora_cni_score(
    resource_allocation_score: float,
    ra_cni_score: float,
    source,
    target,
    neighbors: dict,
    degrees: dict,
    beta: float
) -> float:
    third_order_score = 0.0

    for node_p in neighbors[source]:
        for node_q in neighbors[node_p]:
            if node_q == source or node_q == target:
                continue

            if target in neighbors[node_q]:
                denominator = degrees[node_p] * degrees[node_q]
                third_order_score += safe_divide(1.0, denominator)

    return float(ra_cni_score + beta * third_order_score)

def compute_pair_scores(
    source,
    target,
    cache: dict,
    lpi_beta_values: list[float],
    quasi_local_step_values: list[int],
    ora_cni_beta_values: list[float],
    friendlink_step_values: list[int]
) -> dict:
    neighbors = cache["neighbors"]
    degrees = cache["degrees"]
    average_degree = cache["average_degree"]
    prior_odds = cache["prior_odds"]
    local_role = cache["local_role"]

    source_neighbors = neighbors[source]
    target_neighbors = neighbors[target]

    common_neighbors = source_neighbors.intersection(target_neighbors)
    union_neighbors = source_neighbors.union(target_neighbors)

    common_neighbors_count = len(common_neighbors)

    source_degree = degrees[source]
    target_degree = degrees[target]

    jaccard_score = safe_divide(common_neighbors_count, len(union_neighbors))

    adamic_adar_score = sum(
        safe_divide(1.0, math.log(degrees[common_neighbor]))
        for common_neighbor in common_neighbors
        if degrees[common_neighbor] > 1
    )

    resource_allocation_score = sum(
        safe_divide(1.0, degrees[common_neighbor])
        for common_neighbor in common_neighbors
        if degrees[common_neighbor] > 0
    )

    ra_cni_score = compute_ra_cni_score(
        source_neighbors=source_neighbors,
        target_neighbors=target_neighbors,
        neighbors=neighbors,
        degrees=degrees,
        base_resource_allocation_score=resource_allocation_score
    )

    preferential_attachment_score = source_degree * target_degree

    salton_score = safe_divide(
        common_neighbors_count,
        math.sqrt(source_degree * target_degree)
    )

    sorensen_score = safe_divide(
        2 * common_neighbors_count,
        source_degree + target_degree
    )

    hub_promoted_score = safe_divide(
        common_neighbors_count,
        min(source_degree, target_degree)
    )

    hub_depressed_score = safe_divide(
        common_neighbors_count,
        max(source_degree, target_degree)
    )

    local_leicht_holme_newman_score = safe_divide(
        common_neighbors_count,
        source_degree * target_degree
    )

    common_neighbor_internal_links = count_edges_inside_node_set(
        node_set=common_neighbors,
        neighbors=neighbors
    )

    individual_attraction_2_score = 0.0

    if common_neighbors_count > 0:
        individual_attraction_2_score = sum(
            safe_divide(
                common_neighbor_internal_links + 2,
                degrees[common_neighbor] * common_neighbors_count
            )
            for common_neighbor in common_neighbors
            if degrees[common_neighbor] > 0
        )

    lnb_resource_allocation_score = 0.0

    for common_neighbor in common_neighbors:
        degree = degrees[common_neighbor]
        role = local_role[common_neighbor]
        log_term = safe_log(prior_odds * role)

        if degree > 0:
            lnb_resource_allocation_score += safe_divide(1.0, degree) * log_term

    car_resource_allocation_score = sum(
        safe_divide(
            len(neighbors[common_neighbor].intersection(common_neighbors)),
            degrees[common_neighbor]
        )
        for common_neighbor in common_neighbors
        if degrees[common_neighbor] > 0
    )

    fsw_score = compute_fsw_score(
        source_neighbors=source_neighbors,
        target_neighbors=target_neighbors,
        common_neighbors_count=common_neighbors_count,
        average_degree=average_degree
    )

    path_counts_by_length = {}

    if len(lpi_beta_values) > 0:
        maximum_path_length = max([3] + list(quasi_local_step_values))

        path_counts_by_length = count_walks_by_length_between_pair(
            source=source,
            target=target,
            neighbors=neighbors,
            maximum_length=maximum_path_length
        )

    pair_scores = {
        "score_common_neighbors": float(common_neighbors_count),
        "score_adamic_adar": float(adamic_adar_score),
        "score_resource_allocation": float(resource_allocation_score),
        "score_ra_cni": float(ra_cni_score),
        "score_preferential_attachment": float(preferential_attachment_score),
        "score_jaccard": float(jaccard_score),
        "score_salton": float(salton_score),
        "score_sorensen": float(sorensen_score),
        "score_hub_promoted": float(hub_promoted_score),
        "score_hub_depressed": float(hub_depressed_score),
        "score_local_leicht_holme_newman": float(local_leicht_holme_newman_score),
        "score_individual_attraction_2": float(individual_attraction_2_score),
        "score_lnb_resource_allocation": float(lnb_resource_allocation_score),
        "score_car_resource_allocation": float(car_resource_allocation_score),
        "score_functional_similarity_weight": float(fsw_score),
    }

    for beta in lpi_beta_values:
        beta_label = format_parameter_value(beta)

        pair_scores[f"score_local_path_beta_{beta_label}"] = compute_lpi_score(
            path_counts_by_length=path_counts_by_length,
            beta=beta,
            maximum_length=3
        )

    return pair_scores

def add_lit_scores(
    train_graph: nx.Graph,
    scored_candidate_table: pd.DataFrame,
    iteration_values: list[int]
) -> pd.DataFrame:
    scored_candidate_table = scored_candidate_table.copy()

    cache = build_topological_cache(train_graph)
    neighbors = cache["neighbors"]
    number_of_nodes = cache["number_of_nodes"]

    max_iteration = max(iteration_values)

    train_edges = get_normalized_edge_set(train_graph)

    candidate_pairs = [
        normalize_edge((row.source, row.target))
        for row in scored_candidate_table.itertuples(index=False)
    ]

    required_pairs = set(candidate_pairs).union(train_edges)

    previous_scores = {
        edge: 1.0 if edge in train_edges else 0.0
        for edge in required_pairs
    }

    for iteration in range(1, max_iteration + 1):
        incident_score_sum = defaultdict(float)

        for (node_u, node_v), score in previous_scores.items():
            incident_score_sum[node_u] += score
            incident_score_sum[node_v] += score

        current_scores = {}

        for source, target in required_pairs:
            common_neighbors = neighbors[source].intersection(neighbors[target])

            source_common_sum = sum(
                previous_scores.get(normalize_edge((common_neighbor, source)), 0.0)
                for common_neighbor in common_neighbors
            )

            target_common_sum = sum(
                previous_scores.get(normalize_edge((common_neighbor, target)), 0.0)
                for common_neighbor in common_neighbors
            )

            numerator = source_common_sum + target_common_sum

            source_denominator = sum(
                previous_scores.get(normalize_edge((neighbor, source)), 0.0)
                for neighbor in neighbors[source]
            )

            target_denominator = sum(
                previous_scores.get(normalize_edge((neighbor, target)), 0.0)
                for neighbor in neighbors[target]
            )

            lambda_source = max(
                0.0,
                safe_divide(incident_score_sum[source], number_of_nodes) - source_common_sum
            )

            lambda_target = max(
                0.0,
                safe_divide(incident_score_sum[target], number_of_nodes) - target_common_sum
            )

            denominator = (
                source_denominator
                + target_denominator
                + lambda_source
                + lambda_target
            )

            current_scores[(source, target)] = safe_divide(numerator, denominator)

        previous_scores = current_scores

        if iteration in iteration_values:
            column_name = f"score_lit_t{iteration}"

            scored_candidate_table[column_name] = [
                previous_scores.get(pair, 0.0)
                for pair in candidate_pairs
            ]

    return scored_candidate_table


"""## **7. Analysis of Fold Robustness**

Following the final consolidation of results, this step assesses the robustness of the methods with respect to the five folds used in the experimental protocol.

Robustness analysis checks whether the performance of each method remains stable across folds within the same network or if it varies significantly depending on the edges removed for testing. This evaluation is important because a method might show good average performance but low stability across folds.

This step will produce:

-   statistics of mean, standard deviation, minimum, maximum, and range of metrics per network and method;
-   coefficient of variation for the main metrics;
-   stability of method rankings across folds;
-   identification of the most robust methods per network;
-   a general summary of method robustness;
-   saving and validation of the files from this stage.
"""


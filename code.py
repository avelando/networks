def validate_final_figure_outputs(
    final_figures_dir: Path,
    final_figure_index: pd.DataFrame,
    final_figure_summary: pd.DataFrame,
    required_paths: list[Path],
    output_manifest: pd.DataFrame
) -> None:
    assert final_figures_dir.exists(), (
        f"Final figures directory not found: {final_figures_dir}"
    )

    assert isinstance(final_figure_index, pd.DataFrame), (
        "final_figure_index is not a pandas DataFrame."
    )

    assert isinstance(final_figure_summary, pd.DataFrame), (
        "final_figure_summary is not a pandas DataFrame."
    )

    assert len(final_figure_index) > 0, (
        "final_figure_index is empty."
    )

    assert len(final_figure_summary) > 0, (
        "final_figure_summary is empty."
    )

    for path in required_paths:
        assert path.exists(), f"Expected final figure output not found: {path}"

    expected_columns = {
        "stage",
        "figure_name",
        "description",
        "format",
        "path",
    }

    missing_columns = expected_columns - set(final_figure_index.columns)

    assert len(missing_columns) == 0, (
        f"Missing columns in final_figure_index: {missing_columns}"
    )

    assert set(final_figure_index["format"]).issubset({"png", "pdf"}), (
        "Unexpected figure format found in final_figure_index."
    )

    figure_paths = final_figure_index["path"].apply(Path)

    missing_figure_paths = [
        path for path in figure_paths
        if not path.exists()
    ]

    assert len(missing_figure_paths) == 0, (
        f"Some final figure files do not exist: {missing_figure_paths}"
    )

    empty_figure_paths = [
        path for path in figure_paths
        if path.stat().st_size == 0
    ]

    assert len(empty_figure_paths) == 0, (
        f"Some final figure files are empty: {empty_figure_paths}"
    )

    format_counts = final_figure_index["format"].value_counts().to_dict()

    assert format_counts.get("png", 0) > 0, (
        "No PNG figures were generated."
    )

    assert format_counts.get("pdf", 0) > 0, (
        "No PDF figures were generated."
    )

    assert format_counts.get("png", 0) == format_counts.get("pdf", 0), (
        "The number of PNG and PDF figures does not match."
    )

    manifest_paths = output_manifest["path"].apply(Path)

    missing_manifest_paths = [
        path for path in manifest_paths
        if not path.exists()
    ]

    assert len(missing_manifest_paths) == 0, (
        f"Some files listed in output_manifest do not exist: {missing_manifest_paths}"
    )

    print("Final figure outputs were successfully validated.")


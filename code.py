def standardize_graph(graph: nx.Graph, use_largest_component: bool = True) -> nx.Graph:
    standardized_graph = nx.Graph(graph)
    standardized_graph.remove_edges_from(nx.selfloop_edges(standardized_graph))

    if use_largest_component and standardized_graph.number_of_nodes() > 0:
        if not nx.is_connected(standardized_graph):
            largest_component_nodes = max(nx.connected_components(standardized_graph), key=len)
            standardized_graph = standardized_graph.subgraph(largest_component_nodes).copy()

    return standardized_graph

def load_snap_edge_list(file_path: Path, directed: bool = False) -> nx.Graph:
    graph_type = nx.DiGraph() if directed else nx.Graph()


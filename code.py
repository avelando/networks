def compute_graph_summary(raw_graphs: dict[str, nx.Graph], graphs: dict[str, nx.Graph]) -> pd.DataFrame:
    summary_rows = []

    for network_name, graph in graphs.items():
        raw_graph = raw_graphs[network_name]

        raw_nodes = raw_graph.number_of_nodes()
        raw_edges = raw_graph.number_of_edges()

        raw_as_undirected = nx.Graph(raw_graph)
        raw_as_undirected.remove_edges_from(nx.selfloop_edges(raw_as_undirected))

        raw_undirected_nodes = raw_as_undirected.number_of_nodes()
        raw_undirected_edges = raw_as_undirected.number_of_edges()
        raw_components = nx.number_connected_components(raw_as_undirected)

        clean_nodes = graph.number_of_nodes()
        clean_edges = graph.number_of_edges()

        removed_nodes = raw_undirected_nodes - clean_nodes
        removed_edges = raw_undirected_edges - clean_edges

        largest_component_node_ratio = (
            clean_nodes / raw_undirected_nodes
            if raw_undirected_nodes > 0
            else np.nan
        )

        largest_component_edge_ratio = (
            clean_edges / raw_undirected_edges
            if raw_undirected_edges > 0
            else np.nan
        )

        average_degree = (
            2 * clean_edges / clean_nodes
            if clean_nodes > 0
            else np.nan
        )

        summary_rows.append({
            "network": network_name,
            "domain": DATASETS[network_name]["domain"],
            "source": DATASETS[network_name]["source"],
            "raw_graph_type": type(raw_graph).__name__,
            "clean_graph_type": type(graph).__name__,
            "raw_nodes": raw_nodes,
            "raw_edges": raw_edges,
            "raw_undirected_nodes": raw_undirected_nodes,
            "raw_undirected_edges": raw_undirected_edges,
            "raw_components": raw_components,
            "clean_nodes": clean_nodes,
            "clean_edges": clean_edges,
            "removed_nodes": removed_nodes,
            "removed_edges": removed_edges,
            "largest_component_node_ratio": largest_component_node_ratio,
            "largest_component_edge_ratio": largest_component_edge_ratio,
            "density": nx.density(graph),
            "average_degree": average_degree,
            "average_clustering": nx.average_clustering(graph),
            "transitivity": nx.transitivity(graph),
            "degree_assortativity": safe_degree_assortativity(graph),
        })

    return pd.DataFrame(summary_rows)


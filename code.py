def compute_method_structural_correlations(
    evaluation_with_structure: pd.DataFrame,
    structural_metrics: list[str],
    performance_metric: str = "average_precision"
) -> pd.DataFrame:
    correlation_rows = []

    for method_name, method_data in evaluation_with_structure.groupby("method"):
        for structural_metric in structural_metrics:
            analysis_data = method_data[[structural_metric, performance_metric]].dropna()

            if len(analysis_data) < 3:
                pearson_correlation = np.nan
                spearman_correlation = np.nan
            elif analysis_data[structural_metric].nunique() <= 1:
                pearson_correlation = np.nan
                spearman_correlation = np.nan
            elif analysis_data[performance_metric].nunique() <= 1:
                pearson_correlation = np.nan
                spearman_correlation = np.nan
            else:
                pearson_correlation = analysis_data.corr(
                    method="pearson"
                ).iloc[0, 1]

                spearman_correlation = analysis_data.corr(
                    method="spearman"
                ).iloc[0, 1]

            correlation_rows.append({
                "method": method_name,
                "method_family": assign_method_family(method_name),
                "structural_metric": structural_metric,
                "performance_metric": performance_metric,
                "pearson_correlation": pearson_correlation,
                "spearman_correlation": spearman_correlation,
                "absolute_spearman_correlation": (
                    abs(spearman_correlation)
                    if pd.notna(spearman_correlation)
                    else np.nan
                ),
                "networks_used": len(analysis_data),
            })

    return pd.DataFrame(correlation_rows)


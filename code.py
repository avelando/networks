def plot_metric_heatmap(
    evaluation_results: pd.DataFrame,
    metric: str,
    title: str,
    top_n_methods: int | None = None
) -> None:
    pivot_table = evaluation_results.pivot(
        index="method",
        columns="network",
        values=metric
    )

    method_order = (
        pivot_table
        .mean(axis=1)
        .sort_values(ascending=False)
        .index
    )

    pivot_table = pivot_table.loc[method_order]

    if top_n_methods is not None:
        pivot_table = pivot_table.head(top_n_methods)

    figure_height = max(6, 0.35 * len(pivot_table))

    plt.figure(figsize=(12, figure_height))
    image = plt.imshow(pivot_table.values, aspect="auto")

    plt.title(title)
    plt.xlabel("Network")
    plt.ylabel("Method")

    plt.xticks(
        ticks=np.arange(len(pivot_table.columns)),
        labels=pivot_table.columns,
        rotation=30,
        ha="right"
    )

    plt.yticks(
        ticks=np.arange(len(pivot_table.index)),
        labels=pivot_table.index
    )

    plt.colorbar(image, label=metric)

    for row_index in range(pivot_table.shape[0]):
        for column_index in range(pivot_table.shape[1]):
            value = pivot_table.iloc[row_index, column_index]

            if pd.notna(value):
                plt.text(
                    column_index,
                    row_index,
                    f"{value:.3f}",
                    ha="center",
                    va="center",
                    fontsize=8
                )

    plt.tight_layout()
    plt.show()


def build_network_family_difficulty_table(
    family_performance_by_network: pd.DataFrame,
    metric: str = "mean_average_precision"
) -> pd.DataFrame:
    rows = []

    assert metric in family_performance_by_network.columns, (
        f"Metric not found in family_performance_by_network: {metric}"
    )

    for network, network_data in family_performance_by_network.groupby("network"):
        valid_data = network_data.dropna(subset=[metric]).copy()

        if len(valid_data) == 0:
            continue

        best_family_row = valid_data.sort_values(
            metric,
            ascending=False,
            kind="mergesort"
        ).iloc[0]

        worst_family_row = valid_data.sort_values(
            metric,
            ascending=True,
            kind="mergesort"
        ).iloc[0]

        rows.append({
            "network": network,
            "family_count": valid_data["method_family"].nunique(),
            "mean_family_performance": float(valid_data[metric].mean()),
            "median_family_performance": float(valid_data[metric].median()),
            "std_family_performance": float(valid_data[metric].std(ddof=1))
            if len(valid_data) > 1 else 0.0,
            "best_family": best_family_row["method_family"],
            "best_family_performance": float(best_family_row[metric]),
            "worst_family": worst_family_row["method_family"],
            "worst_family_performance": float(worst_family_row[metric]),
            "family_performance_range": float(
                best_family_row[metric] - worst_family_row[metric]
            ),
        })

    table = pd.DataFrame(rows)

    table = table.sort_values(
        by=["mean_family_performance", "best_family_performance"],
        ascending=[True, True],
        kind="mergesort"
    ).reset_index(drop=True)

    table["family_difficulty_rank"] = np.arange(1, len(table) + 1)

    ordered_columns = [
        "family_difficulty_rank",
        "network",
        "family_count",
        "mean_family_performance",
        "median_family_performance",
        "std_family_performance",
        "best_family",
        "best_family_performance",
        "worst_family",
        "worst_family_performance",
        "family_performance_range",
    ]

    return table[ordered_columns]


def compute_random_walk_distributions_from_source(
    source,
    neighbors: dict,
    degrees: dict,
    max_steps: int
) -> tuple[dict[int, dict], dict[int, dict]]:
    current_distribution = {source: 1.0}
    walk_distributions = {}
    superposed_distributions = {}

    accumulated_distribution = defaultdict(float)

    for step in range(1, max_steps + 1):
        next_distribution = defaultdict(float)

        for node, probability in current_distribution.items():
            degree = degrees[node]

            if degree == 0:
                continue

            transition_probability = probability / degree

            for neighbor in neighbors[node]:
                next_distribution[neighbor] += transition_probability

        current_distribution = dict(next_distribution)

        for node, probability in current_distribution.items():
            accumulated_distribution[node] += probability

        walk_distributions[step] = dict(current_distribution)
        superposed_distributions[step] = dict(accumulated_distribution)

    return walk_distributions, superposed_distributions

def add_local_random_walk_scores(
    train_graph: nx.Graph,
    scored_candidate_table: pd.DataFrame,
    step_values: list[int]
) -> pd.DataFrame:
    scored_candidate_table = scored_candidate_table.copy()

    cache = build_topological_cache(train_graph)
    neighbors = cache["neighbors"]
    degrees = cache["degrees"]
    two_m = 2 * train_graph.number_of_edges()

    max_steps = max(step_values)

    for step in step_values:
        scored_candidate_table[f"score_lrw_l{step}"] = 0.0
        scored_candidate_table[f"score_srw_l{step}"] = 0.0

    source_groups = scored_candidate_table.groupby("source").groups
    target_groups = scored_candidate_table.groupby("target").groups

    candidate_nodes = sorted(
        set(scored_candidate_table["source"]).union(set(scored_candidate_table["target"]))
    )

    for node in tqdm(
        candidate_nodes,
        total=len(candidate_nodes),
        desc="Computing LRW/SRW scores"
    ):
        walk_distributions, superposed_distributions = compute_random_walk_distributions_from_source(
            source=node,
            neighbors=neighbors,
            degrees=degrees,
            max_steps=max_steps
        )

        node_weight = safe_divide(degrees[node], two_m)

        if node in source_groups:
            indices = list(source_groups[node])
            targets = scored_candidate_table.loc[indices, "target"]

            for step in step_values:
                walk_distribution = walk_distributions[step]
                superposed_distribution = superposed_distributions[step]

                scored_candidate_table.loc[indices, f"score_lrw_l{step}"] += [
                    node_weight * walk_distribution.get(target, 0.0)
                    for target in targets
                ]

                scored_candidate_table.loc[indices, f"score_srw_l{step}"] += [
                    node_weight * superposed_distribution.get(target, 0.0)
                    for target in targets
                ]

        if node in target_groups:
            indices = list(target_groups[node])
            sources = scored_candidate_table.loc[indices, "source"]

            for step in step_values:
                walk_distribution = walk_distributions[step]
                superposed_distribution = superposed_distributions[step]

                scored_candidate_table.loc[indices, f"score_lrw_l{step}"] += [
                    node_weight * walk_distribution.get(source, 0.0)
                    for source in sources
                ]

                scored_candidate_table.loc[indices, f"score_srw_l{step}"] += [
                    node_weight * superposed_distribution.get(source, 0.0)
                    for source in sources
                ]

    return scored_candidate_table

def compute_propflow_from_source(
    source,
    neighbors: dict,
    degrees: dict,
    max_steps: int
) -> dict[int, dict]:
    scores = defaultdict(float)
    scores[source] = 1.0

    found_nodes = {source}
    new_search = {source}

    step_scores = {}

    for current_degree in range(1, max_steps + 1):
        old_search = set(new_search)
        new_search = set()

        for node_i in old_search:
            degree_i = degrees[node_i]

            if degree_i == 0:
                continue

            for node_j in neighbors[node_i]:
                scores[node_j] += scores[node_i] / degree_i

                if node_j not in found_nodes:
                    found_nodes.add(node_j)
                    new_search.add(node_j)

        step_scores[current_degree] = dict(scores)

    return step_scores


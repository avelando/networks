def safe_package_version(package_name: str) -> str:
    try:
        return importlib.metadata.version(package_name)
    except importlib.metadata.PackageNotFoundError:
        return "not_installed"

def compute_file_sha256(file_path: Path, chunk_size: int = 1024 * 1024) -> str:
    sha256_hash = hashlib.sha256()

    with open(file_path, "rb") as file:
        for chunk in iter(lambda: file.read(chunk_size), b""):
            sha256_hash.update(chunk)

    return sha256_hash.hexdigest()

def get_relative_path_for_export(file_path: Path) -> str:
    file_path = Path(file_path)

    try:
        return str(file_path.relative_to(Path(".")))
    except ValueError:
        return str(file_path)

def collect_generated_file_inventory(
    root_dir: Path,
    excluded_paths: set[Path] | None = None
) -> pd.DataFrame:
    if excluded_paths is None:
        excluded_paths = set()

    excluded_resolved_paths = {
        Path(path).resolve()
        for path in excluded_paths
    }

    rows = []

    for file_path in sorted(root_dir.rglob("*")):
        if not file_path.is_file():
            continue

        resolved_path = file_path.resolve()

        if resolved_path in excluded_resolved_paths:
            continue

        rows.append({
            "relative_path": get_relative_path_for_export(file_path),
            "absolute_path": str(resolved_path),
            "size_bytes": int(file_path.stat().st_size),
            "sha256": compute_file_sha256(file_path),
        })

    inventory = pd.DataFrame(rows)

    if len(inventory) > 0:
        inventory = inventory.sort_values("relative_path").reset_index(drop=True)

    return inventory


def solve_sparse_columns(
    matrix: sparse.csr_matrix,
    target_indices: list[int]
) -> dict[int, np.ndarray]:
    solver = sparse_linalg.factorized(matrix.tocsc())
    solved_columns = {}

    number_of_nodes = matrix.shape[0]

    for target_index in tqdm(target_indices, desc="Solving sparse linear systems"):
        basis_vector = np.zeros(number_of_nodes, dtype=float)
        basis_vector[target_index] = 1.0
        solved_columns[target_index] = solver(basis_vector)

    return solved_columns

def add_katz_scores(
    train_graph: nx.Graph,
    scored_candidate_table: pd.DataFrame,
    beta_values: list[float]
) -> pd.DataFrame:
    scored_candidate_table = scored_candidate_table.copy()

    cache = build_topological_cache(train_graph)
    adjacency_matrix = cache["adjacency_matrix"]
    node_to_index = cache["node_to_index"]

    number_of_nodes = train_graph.number_of_nodes()
    identity_matrix = sparse.identity(number_of_nodes, format="csr")

    target_indices = sorted(
        scored_candidate_table["target"].map(node_to_index).unique()
    )

    for beta in beta_values:
        beta_label = format_parameter_value(beta)
        column_name = f"score_katz_beta_{beta_label}"

        system_matrix = identity_matrix - beta * adjacency_matrix

        solved_columns = solve_sparse_columns(
            matrix=system_matrix,
            target_indices=target_indices
        )

        scores = []

        for _, row in scored_candidate_table.iterrows():
            source_index = node_to_index[row["source"]]
            target_index = node_to_index[row["target"]]

            raw_value = solved_columns[target_index][source_index]

            if source_index == target_index:
                raw_value -= 1.0

            scores.append(float(raw_value))

        scored_candidate_table[column_name] = scores

    return scored_candidate_table

def add_global_lhn_scores(
    train_graph: nx.Graph,
    scored_candidate_table: pd.DataFrame,
    beta_values: list[float]
) -> pd.DataFrame:
    scored_candidate_table = scored_candidate_table.copy()

    cache = build_topological_cache(train_graph)
    adjacency_matrix = cache["adjacency_matrix"]
    node_to_index = cache["node_to_index"]
    degrees_array = cache["degrees_array"]

    number_of_nodes = train_graph.number_of_nodes()
    number_of_edges = train_graph.number_of_edges()

    identity_matrix = sparse.identity(number_of_nodes, format="csr")

    eigenvalue = sparse_linalg.eigsh(
        adjacency_matrix,
        k=1,
        which="LM",
        return_eigenvectors=False
    )[0]

    target_indices = sorted(
        scored_candidate_table["target"].map(node_to_index).unique()
    )

    for beta in beta_values:
        beta_label = format_parameter_value(beta)
        column_name = f"score_global_lhn_beta_{beta_label}"

        system_matrix = identity_matrix - (beta / eigenvalue) * adjacency_matrix

        solved_columns = solve_sparse_columns(
            matrix=system_matrix,
            target_indices=target_indices
        )

        scores = []

        for _, row in scored_candidate_table.iterrows():
            source_index = node_to_index[row["source"]]
            target_index = node_to_index[row["target"]]

            source_degree = degrees_array[source_index]
            target_degree = degrees_array[target_index]

            raw_value = solved_columns[target_index][source_index]

            score = safe_divide(
                2 * number_of_edges * eigenvalue * raw_value,
                source_degree * target_degree
            )

            scores.append(float(score))

        scored_candidate_table[column_name] = scores

    return scored_candidate_table

def compute_iterative_walk_distribution(
    transition_matrix_transposed: sparse.csr_matrix,
    source_index: int,
    alpha: float | None = None,
    restart: bool = False,
    tolerance: float = 1e-8,
    max_iterations: int = 100
) -> np.ndarray:
    number_of_nodes = transition_matrix_transposed.shape[0]

    seed_vector = np.zeros(number_of_nodes, dtype=float)
    seed_vector[source_index] = 1.0

    probability_vector = seed_vector.copy()

    for _ in range(max_iterations):
        if restart:
            next_vector = (
                alpha * (transition_matrix_transposed @ probability_vector)
                + (1 - alpha) * seed_vector
            )
        else:
            next_vector = transition_matrix_transposed @ probability_vector

        difference = np.linalg.norm(next_vector - probability_vector)

        probability_vector = next_vector

        if difference < tolerance:
            break

    return probability_vector


def strip_cross_validation_suffix(metric_name: str) -> str:
    if metric_name.endswith("_mean"):
        return metric_name[:-5]

    return metric_name

def build_network_method_performance_summary(
    cross_validation_results: pd.DataFrame,
    metrics: list[str]
) -> pd.DataFrame:
    aggregation_spec = {
        "method_count": ("method", "nunique"),
    }

    for metric in metrics:
        metric_label = strip_cross_validation_suffix(metric)

        aggregation_spec[f"mean_{metric_label}"] = (metric, "mean")
        aggregation_spec[f"median_{metric_label}"] = (metric, "median")
        aggregation_spec[f"std_{metric_label}"] = (metric, "std")
        aggregation_spec[f"best_{metric_label}"] = (metric, "max")
        aggregation_spec[f"worst_{metric_label}"] = (metric, "min")

    summary = (
        cross_validation_results
        .groupby("network")
        .agg(**aggregation_spec)
        .reset_index()
    )

    for metric in metrics:
        metric_label = strip_cross_validation_suffix(metric)

        summary[f"range_{metric_label}"] = (
            summary[f"best_{metric_label}"]
            - summary[f"worst_{metric_label}"]
        )

        summary[f"std_{metric_label}"] = summary[f"std_{metric_label}"].fillna(0.0)

    return summary


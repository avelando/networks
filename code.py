def detect_visualization_communities(graph: nx.Graph) -> dict:
    if graph.number_of_edges() == 0:
        return {node: 0 for node in graph.nodes()}

    try:
        communities = list(
            nx.algorithms.community.louvain_communities(
                graph,
                seed=RANDOM_STATE
            )
        )
    except Exception:
        communities = list(greedy_modularity_communities(graph))

    node_to_community = {}

    for community_id, community_nodes in enumerate(communities):
        for node in community_nodes:
            node_to_community[node] = community_id

    return node_to_community

def compute_node_sizes_for_full_visualization(
    graph: nx.Graph,
    minimum_size: float = 1.5,
    maximum_size: float = 45.0
) -> list:
    degrees = dict(graph.degree())

    if len(degrees) == 0:
        return []

    max_degree = max(degrees.values())

    if max_degree == 0:
        return [minimum_size for _ in graph.nodes()]

    node_sizes = [
        minimum_size + (maximum_size - minimum_size) * (degrees[node] / max_degree)
        for node in graph.nodes()
    ]

    return node_sizes

def choose_layout_iterations(graph: nx.Graph) -> int:
    number_of_nodes = graph.number_of_nodes()

    if number_of_nodes <= 1500:
        return 100

    if number_of_nodes <= 5000:
        return 70

    return 40

def compute_full_graph_layout(
    graph: nx.Graph,
    random_state: int = 42
) -> dict:
    layout_iterations = choose_layout_iterations(graph)

    position = nx.spring_layout(
        graph,
        seed=random_state,
        iterations=layout_iterations,
        k=None
    )

    return position

def plot_full_network_topology(
    graph: nx.Graph,
    network_name: str,
    output_dir: Path,
    random_state: int = 42,
    show_labels: bool = False
) -> dict:
    visualization_graph = graph.copy()

    node_to_community = detect_visualization_communities(visualization_graph)

    node_colors = [
        node_to_community.get(node, 0)
        for node in visualization_graph.nodes()
    ]

    node_sizes = compute_node_sizes_for_full_visualization(
        graph=visualization_graph,
        minimum_size=1.5,
        maximum_size=45.0
    )

    edge_alpha = 0.06
    edge_width = 0.12

    if visualization_graph.number_of_edges() <= 10000:
        edge_alpha = 0.14
        edge_width = 0.25

    if visualization_graph.number_of_edges() <= 3000:
        edge_alpha = 0.25
        edge_width = 0.45

    position = compute_full_graph_layout(
        graph=visualization_graph,
        random_state=random_state
    )

    plt.figure(figsize=(16, 12))

    nx.draw_networkx_edges(
        visualization_graph,
        position,
        alpha=edge_alpha,
        width=edge_width
    )

    nx.draw_networkx_nodes(
        visualization_graph,
        position,
        node_size=node_sizes,
        node_color=node_colors,
        cmap=plt.cm.tab20,
        alpha=0.92,
        linewidths=0.05,
        edgecolors="black"
    )

    if show_labels:
        degree_values = [degree for _, degree in visualization_graph.degree()]
        label_threshold = np.percentile(degree_values, 99)

        labels = {
            node: str(node)
            for node, degree in visualization_graph.degree()
            if degree >= label_threshold
        }

        nx.draw_networkx_labels(
            visualization_graph,
            position,
            labels=labels,
            font_size=6
        )

    original_nodes = graph.number_of_nodes()
    original_edges = graph.number_of_edges()
    visualized_nodes = visualization_graph.number_of_nodes()
    visualized_edges = visualization_graph.number_of_edges()

    plt.title(
        f"{network_name}\n"
        f"Full network: {visualized_nodes} nodes, {visualized_edges} edges"
    )

    plt.axis("off")
    plt.tight_layout()

    output_path = output_dir / f"{network_name}_full_topology.png"

    plt.savefig(
        output_path,
        dpi=300,
        bbox_inches="tight"
    )

    plt.show()

    return {
        "network": network_name,
        "original_nodes": original_nodes,
        "original_edges": original_edges,
        "visualized_nodes": visualized_nodes,
        "visualized_edges": visualized_edges,
        "is_full_network": visualized_nodes == original_nodes and visualized_edges == original_edges,
        "image_path": str(output_path),
    }


"""## **4. Evaluation of Link Prediction Methods**

After computing the scores for link prediction methods, this step evaluates the performance of each method on each network.

The evaluation compares positive pairs, which correspond to actual edges hidden in the previous step, with negative pairs, which correspond to unconnected node pairs in the original network. A method performs well when it assigns higher scores to positive pairs than to negative pairs.

The metrics used are:

-   **ROC-AUC**: measures the overall ability to distinguish between positive and negative pairs;
-   **Average Precision**: assesses the quality of the ranking considering the order of positive pairs;
-   **Precision@K**: measures the proportion of positive pairs among the top K ranked items;
-   **Recall@K**: measures the proportion of retrieved positive pairs among the top K ranked items;
-   **NDCG@K**: evaluates the quality of the ranking, giving more weight to hits in the top positions.

The metric for `K = number of positives` is also calculated, meaning it considers a number of predictions equal to the number of edges removed for testing.
"""

from sklearn.metrics import roc_auc_score, average_precision_score

EVALUATION_K_VALUES = [10, 50, 100, 500]
PRIMARY_EVALUATION_METRIC = "average_precision"

def top_k_confusion_counts(labels: np.ndarray, k: int) -> dict:
    if len(labels) == 0:
        return {
            "tp": 0,
            "fp": 0,
            "fn": 0,
            "tn": 0,
            "effective_k": 0,
        }

    effective_k = min(k, len(labels))

    top_k_labels = labels[:effective_k]
    remaining_labels = labels[effective_k:]

    tp = int(np.sum(top_k_labels == 1))
    fp = int(np.sum(top_k_labels == 0))
    fn = int(np.sum(remaining_labels == 1))
    tn = int(np.sum(remaining_labels == 0))

    return {
        "tp": tp,
        "fp": fp,
        "fn": fn,
        "tn": tn,
        "effective_k": effective_k,
    }

def precision_from_counts(tp: int, fp: int) -> float:
    return safe_divide(tp, tp + fp)

def recall_from_counts(tp: int, fn: int) -> float:
    return safe_divide(tp, tp + fn)

def specificity_from_counts(tn: int, fp: int) -> float:
    return safe_divide(tn, tn + fp)

def false_positive_rate_from_counts(fp: int, tn: int) -> float:
    return safe_divide(fp, fp + tn)

def accuracy_from_counts(tp: int, fp: int, fn: int, tn: int) -> float:
    return safe_divide(tp + tn, tp + fp + fn + tn)

def balanced_accuracy_from_counts(tp: int, fp: int, fn: int, tn: int) -> float:
    recall = recall_from_counts(tp, fn)
    specificity = specificity_from_counts(tn, fp)

    return (recall + specificity) / 2

def f1_from_precision_recall(precision: float, recall: float) -> float:
    return safe_divide(2 * precision * recall, precision + recall)

def dcg_at_k(labels: np.ndarray, k: int) -> float:
    effective_k = min(k, len(labels))

    if effective_k == 0:
        return 0.0

    selected_labels = labels[:effective_k]
    discounts = 1.0 / np.log2(np.arange(2, effective_k + 2))

    return float(np.sum(selected_labels * discounts))

def ndcg_at_k(labels: np.ndarray, k: int) -> float:
    effective_k = min(k, len(labels))

    if effective_k == 0:
        return np.nan

    ideal_labels = np.sort(labels)[::-1]

    dcg = dcg_at_k(labels, effective_k)
    ideal_dcg = dcg_at_k(ideal_labels, effective_k)

    if ideal_dcg == 0:
        return np.nan

    return float(dcg / ideal_dcg)

def hit_rate_at_k(labels: np.ndarray, k: int) -> float:
    effective_k = min(k, len(labels))

    if effective_k == 0:
        return np.nan

    return float(np.sum(labels[:effective_k]) > 0)

def mrr_at_k(labels: np.ndarray, k: int) -> float:
    effective_k = min(k, len(labels))

    if effective_k == 0:
        return np.nan

    positive_positions = np.where(labels[:effective_k] == 1)[0]

    if len(positive_positions) == 0:
        return 0.0

    first_positive_rank = positive_positions[0] + 1

    return float(1.0 / first_positive_rank)

def average_precision_at_k(labels: np.ndarray, k: int) -> float:
    effective_k = min(k, len(labels))

    if effective_k == 0:
        return np.nan

    total_positives = int(np.sum(labels))
    denominator = min(total_positives, effective_k)

    if denominator == 0:
        return np.nan

    precision_sum = 0.0
    positives_seen = 0

    for index, label in enumerate(labels[:effective_k], start=1):
        if label == 1:
            positives_seen += 1
            precision_sum += positives_seen / index

    return float(precision_sum / denominator)

def lift_at_k(labels: np.ndarray, k: int) -> float:
    effective_k = min(k, len(labels))

    if effective_k == 0:
        return np.nan

    positive_ratio = np.mean(labels)

    if positive_ratio == 0:
        return np.nan

    precision = np.sum(labels[:effective_k]) / effective_k

    return float(precision / positive_ratio)

def compute_top_k_metrics(labels: np.ndarray, k: int, prefix: str) -> dict:
    counts = top_k_confusion_counts(labels, k)

    tp = counts["tp"]
    fp = counts["fp"]
    fn = counts["fn"]
    tn = counts["tn"]

    precision = precision_from_counts(tp, fp)
    recall = recall_from_counts(tp, fn)
    specificity = specificity_from_counts(tn, fp)
    false_positive_rate = false_positive_rate_from_counts(fp, tn)
    accuracy = accuracy_from_counts(tp, fp, fn, tn)
    balanced_accuracy = balanced_accuracy_from_counts(tp, fp, fn, tn)
    f1 = f1_from_precision_recall(precision, recall)

    return {
        f"tp_{prefix}": tp,
        f"fp_{prefix}": fp,
        f"fn_{prefix}": fn,
        f"tn_{prefix}": tn,
        f"precision_{prefix}": precision,
        f"recall_{prefix}": recall,
        f"f1_{prefix}": f1,
        f"accuracy_{prefix}": accuracy,
        f"balanced_accuracy_{prefix}": balanced_accuracy,
        f"specificity_{prefix}": specificity,
        f"false_positive_rate_{prefix}": false_positive_rate,
        f"hit_rate_{prefix}": hit_rate_at_k(labels, k),
        f"mrr_{prefix}": mrr_at_k(labels, k),
        f"average_precision_{prefix}": average_precision_at_k(labels, k),
        f"ndcg_{prefix}": ndcg_at_k(labels, k),
        f"lift_{prefix}": lift_at_k(labels, k),
    }

def get_ranked_labels(
    scored_table: pd.DataFrame,
    score_column: str
) -> np.ndarray:
    ranked_table = scored_table.sort_values(
        by=score_column,
        ascending=False,
        kind="mergesort"
    )

    return ranked_table["label"].astype(int).to_numpy()

def evaluate_single_method(
    scored_table: pd.DataFrame,
    method_name: str,
    score_column: str,
    k_values: list[int]
) -> dict:
    y_true = scored_table["label"].astype(int).to_numpy()
    y_scores = scored_table[score_column].astype(float).to_numpy()

    positive_count = int(np.sum(y_true == 1))
    negative_count = int(np.sum(y_true == 0))
    candidate_count = len(y_true)

    unique_score_count = int(pd.Series(y_scores).nunique())

    if len(np.unique(y_true)) < 2:
        roc_auc = np.nan
        average_precision = np.nan
    else:
        roc_auc = float(roc_auc_score(y_true, y_scores))
        average_precision = float(average_precision_score(y_true, y_scores))

    ranked_labels = get_ranked_labels(
        scored_table=scored_table,
        score_column=score_column
    )

    evaluation_row = {
        "network": scored_table["network"].iloc[0],
        "fold_id": int(scored_table["fold_id"].iloc[0]),
        "method": method_name,
        "score_column": score_column,
        "positive_count": positive_count,
        "negative_count": negative_count,
        "candidate_count": candidate_count,
        "unique_score_count": unique_score_count,
        "positive_ratio": positive_count / candidate_count if candidate_count > 0 else np.nan,
        "roc_auc": roc_auc,
        "average_precision": average_precision,
    }

    for k in k_values:
        evaluation_row.update(
            compute_top_k_metrics(
                labels=ranked_labels,
                k=k,
                prefix=f"at_{k}"
            )
        )

    positive_k = max(1, positive_count)

    evaluation_row.update(
        compute_top_k_metrics(
            labels=ranked_labels,
            k=positive_k,
            prefix="at_positive_count"
        )
    )

    return evaluation_row

def evaluate_all_methods(
    scored_candidate_tables: dict[str, pd.DataFrame],
    method_score_columns: dict[str, str],
    k_values: list[int]
) -> pd.DataFrame:
    evaluation_rows = []

    total_iterations = len(scored_candidate_tables) * len(method_score_columns)

    iterator = tqdm(
        [
            (experiment_key, method_name, score_column)
            for experiment_key in scored_candidate_tables.keys()
            for method_name, score_column in method_score_columns.items()
        ],
        total=total_iterations,
        desc="Evaluating methods"
    )

    for experiment_key, method_name, score_column in iterator:
        scored_table = scored_candidate_tables[experiment_key]

        evaluation_row = evaluate_single_method(
            scored_table=scored_table,
            method_name=method_name,
            score_column=score_column,
            k_values=k_values
        )

        evaluation_row["experiment_key"] = experiment_key

        evaluation_rows.append(evaluation_row)

    return pd.DataFrame(evaluation_rows)


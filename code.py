def download_file(url: str, output_path: Path, overwrite: bool = False) -> None:
    if output_path.exists() and not overwrite:
        print(f"File already exists: {output_path}")
        return

    print(f"Downloading: {url}")
    urllib.request.urlretrieve(url, output_path)
    print(f"Saved to: {output_path}")

def extract_gzip(source_path: Path, target_path: Path, overwrite: bool = False) -> None:
    if target_path.exists() and not overwrite:
        print(f"Extracted file already exists: {target_path}")
        return

    print(f"Extracting: {source_path}")
    with gzip.open(source_path, "rb") as input_file:
        with open(target_path, "wb") as output_file:
            shutil.copyfileobj(input_file, output_file)

    print(f"Extracted to: {target_path}")

def extract_zip(source_path: Path, target_dir: Path, overwrite: bool = False) -> None:
    target_dir.mkdir(parents=True, exist_ok=True)

    if any(target_dir.iterdir()) and not overwrite:
        print(f"Zip content already extracted in: {target_dir}")
        return

    print(f"Extracting: {source_path}")
    with zipfile.ZipFile(source_path, "r") as zip_ref:
        zip_ref.extractall(target_dir)

    print(f"Extracted to: {target_dir}")


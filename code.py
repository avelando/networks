def build_topk_confusion_summary_from_evaluation(
    evaluation_results: pd.DataFrame,
    fixed_k_values: list[int]
) -> pd.DataFrame:
    rows = []

    count_metric_names = [
        "tp",
        "fp",
        "fn",
        "tn",
        "precision",
        "recall",
        "f1",
        "accuracy",
        "balanced_accuracy",
        "specificity",
        "false_positive_rate",
        "hit_rate",
        "mrr",
        "average_precision",
        "ndcg",
        "lift",
    ]

    for _, row in evaluation_results.iterrows():
        base_row = {
            "experiment_key": row["experiment_key"],
            "network": row["network"],
            "fold_id": int(row["fold_id"]),
            "method": row["method"],
            "score_column": row["score_column"],
            "positive_count": int(row["positive_count"]),
            "negative_count": int(row["negative_count"]),
            "candidate_count": int(row["candidate_count"]),
        }

        for k in fixed_k_values:
            prefix = f"at_{k}"

            if f"tp_{prefix}" not in evaluation_results.columns:
                continue

            output_row = base_row.copy()
            output_row["k_type"] = "fixed"
            output_row["k_label"] = f"at_{k}"
            output_row["k_value"] = int(k)
            output_row["effective_k"] = min(int(k), int(row["candidate_count"]))

            for metric_name in count_metric_names:
                column_name = f"{metric_name}_{prefix}"

                if column_name in evaluation_results.columns:
                    output_row[metric_name] = row[column_name]

            rows.append(output_row)

        prefix = "at_positive_count"

        if f"tp_{prefix}" in evaluation_results.columns:
            output_row = base_row.copy()
            output_row["k_type"] = "positive_count"
            output_row["k_label"] = "at_positive_count"
            output_row["k_value"] = int(row["positive_count"])
            output_row["effective_k"] = min(
                int(row["positive_count"]),
                int(row["candidate_count"])
            )

            for metric_name in count_metric_names:
                column_name = f"{metric_name}_{prefix}"

                if column_name in evaluation_results.columns:
                    output_row[metric_name] = row[column_name]

            rows.append(output_row)

    return pd.DataFrame(rows)


def assign_network_difficulty_level(
    difficulty_rank: int,
    total_networks: int
) -> str:
    first_cut = int(np.ceil(total_networks / 3))
    second_cut = int(np.ceil(2 * total_networks / 3))

    if difficulty_rank <= first_cut:
        return "high_difficulty"

    if difficulty_rank <= second_cut:
        return "medium_difficulty"

    return "low_difficulty"

def build_network_difficulty_table(
    network_method_performance_summary: pd.DataFrame,
    network_best_worst_methods: pd.DataFrame,
    network_summary: pd.DataFrame,
    network_fold_robustness_summary: pd.DataFrame,
    primary_metric: str
) -> pd.DataFrame:
    primary_label = strip_cross_validation_suffix(primary_metric)

    mean_column = f"mean_{primary_label}"
    median_column = f"median_{primary_label}"
    best_column = f"best_{primary_label}"
    worst_column = f"worst_{primary_label}"
    range_column = f"range_{primary_label}"

    table = network_method_performance_summary.merge(
        network_best_worst_methods,
        on="network",
        how="left"
    )

    structural_columns = [
        "network",
        "domain",
        "source",
        "clean_nodes",
        "clean_edges",
        "density",
        "average_degree",
        "average_clustering",
        "transitivity",
        "degree_assortativity",
    ]

    available_structural_columns = [
        column for column in structural_columns
        if column in network_summary.columns
    ]

    table = table.merge(
        network_summary[available_structural_columns],
        on="network",
        how="left"
    )

    robustness_columns = [
        "network",
        "fold_sensitivity_rank",
        "mean_method_performance",
        "best_method_performance",
        "worst_method_performance",
        "mean_fold_std",
        "median_fold_std",
        "max_fold_std",
        "mean_fold_range",
        "median_fold_range",
        "max_fold_range",
        "mean_fold_cv",
        "median_fold_cv",
    ]

    available_robustness_columns = [
        column for column in robustness_columns
        if column in network_fold_robustness_summary.columns
    ]

    table = table.merge(
        network_fold_robustness_summary[available_robustness_columns],
        on="network",
        how="left"
    )

    rank_columns = []

    if best_column in table.columns:
        table["difficulty_rank_low_best_performance"] = table[best_column].rank(
            ascending=True,
            method="dense"
        )
        rank_columns.append("difficulty_rank_low_best_performance")

    if mean_column in table.columns:
        table["difficulty_rank_low_mean_performance"] = table[mean_column].rank(
            ascending=True,
            method="dense"
        )
        rank_columns.append("difficulty_rank_low_mean_performance")

    if median_column in table.columns:
        table["difficulty_rank_low_median_performance"] = table[median_column].rank(
            ascending=True,
            method="dense"
        )
        rank_columns.append("difficulty_rank_low_median_performance")

    if "fold_sensitivity_rank" in table.columns:
        table["difficulty_rank_fold_sensitivity"] = table["fold_sensitivity_rank"]
        rank_columns.append("difficulty_rank_fold_sensitivity")

    if "mean_fold_std" in table.columns:
        table["difficulty_rank_high_fold_std"] = table["mean_fold_std"].rank(
            ascending=False,
            method="dense"
        )
        rank_columns.append("difficulty_rank_high_fold_std")

    if "mean_fold_range" in table.columns:
        table["difficulty_rank_high_fold_range"] = table["mean_fold_range"].rank(
            ascending=False,
            method="dense"
        )
        rank_columns.append("difficulty_rank_high_fold_range")

    if "mean_fold_cv" in table.columns:
        table["difficulty_rank_high_fold_cv"] = table["mean_fold_cv"].rank(
            ascending=False,
            method="dense"
        )
        rank_columns.append("difficulty_rank_high_fold_cv")

    assert len(rank_columns) > 0, (
        "No rank columns were created for network difficulty analysis."
    )

    table["difficulty_score"] = table[rank_columns].mean(axis=1)

    table["network_difficulty_rank"] = table["difficulty_score"].rank(
        ascending=True,
        method="dense"
    ).astype(int)

    total_networks = table["network"].nunique()

    table["network_difficulty_level"] = table["network_difficulty_rank"].apply(
        lambda rank: assign_network_difficulty_level(
            difficulty_rank=int(rank),
            total_networks=total_networks
        )
    )

    table = table.sort_values(
        by=["network_difficulty_rank", "difficulty_score", best_column],
        ascending=[True, True, True],
        kind="mergesort"
    ).reset_index(drop=True)

    return table


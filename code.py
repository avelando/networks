def add_topk_pair_structural_context(
    topk_prediction_detail: pd.DataFrame,
    train_graphs: dict[str, nx.Graph]
) -> pd.DataFrame:
    enriched_tables = []

    for experiment_key, group in tqdm(
        topk_prediction_detail.groupby("experiment_key"),
        total=topk_prediction_detail["experiment_key"].nunique(),
        desc="Adding structural context to top-K pairs"
    ):
        train_graph = train_graphs[experiment_key]

        degrees = dict(train_graph.degree())
        neighbors = {
            node: set(train_graph.neighbors(node))
            for node in train_graph.nodes()
        }

        enriched_group = group.copy()

        enriched_group["source_degree_train"] = enriched_group["source"].map(degrees)
        enriched_group["target_degree_train"] = enriched_group["target"].map(degrees)

        enriched_group["degree_product_train"] = (
            enriched_group["source_degree_train"]
            * enriched_group["target_degree_train"]
        )

        enriched_group["min_degree_train"] = enriched_group[
            ["source_degree_train", "target_degree_train"]
        ].min(axis=1)

        enriched_group["max_degree_train"] = enriched_group[
            ["source_degree_train", "target_degree_train"]
        ].max(axis=1)

        common_neighbor_counts = []

        for row in enriched_group.itertuples(index=False):
            source_neighbors = neighbors.get(row.source, set())
            target_neighbors = neighbors.get(row.target, set())

            common_neighbor_counts.append(
                len(source_neighbors.intersection(target_neighbors))
            )

        enriched_group["common_neighbors_train"] = common_neighbor_counts

        enriched_tables.append(enriched_group)

    enriched_topk_detail = pd.concat(enriched_tables, ignore_index=True)

    return enriched_topk_detail


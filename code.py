def build_fold_rank_table(
    evaluation_results: pd.DataFrame,
    metric: str
) -> pd.DataFrame:
    assert metric in evaluation_results.columns, (
        f"Metric not found in evaluation_results: {metric}"
    )

    ranked = evaluation_results.sort_values(
        by=["network", "fold_id", metric],
        ascending=[True, True, False],
        kind="mergesort"
    ).copy()

    ranked[f"rank_by_{metric}_within_fold"] = (
        ranked
        .groupby(["network", "fold_id"])[metric]
        .rank(method="dense", ascending=False)
        .astype(int)
    )

    return ranked


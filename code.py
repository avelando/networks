def plot_horizontal_bar_with_labels(
    dataframe: pd.DataFrame,
    x_column: str,
    y_column: str,
    title: str,
    xlabel: str,
    ylabel: str,
    label_column: str | None = None,
    figure_width: float = 10,
    figure_height: float = 6
) -> None:
    plot_data = dataframe.copy()

    plt.figure(figsize=(figure_width, figure_height))
    plt.barh(plot_data[y_column].astype(str), plot_data[x_column].astype(float))

    plt.title(title)
    plt.xlabel(xlabel)
    plt.ylabel(ylabel)

    if label_column is not None and label_column in plot_data.columns:
        for index, (_, row) in enumerate(plot_data.iterrows()):
            x_value = float(row[x_column])
            label_value = str(row[label_column])

            plt.text(
                x_value,
                index,
                f"  {label_value}",
                va="center",
                fontsize=8
            )

    plt.tight_layout()

def plot_heatmap_from_pivot(
    pivot_table: pd.DataFrame,
    title: str,
    xlabel: str,
    ylabel: str,
    colorbar_label: str,
    annotate: bool = True,
    figure_width: float = 12,
    figure_height: float | None = None
) -> None:
    if figure_height is None:
        figure_height = max(6, 0.35 * len(pivot_table))

    plt.figure(figsize=(figure_width, figure_height))

    image = plt.imshow(
        pivot_table.values,
        aspect="auto"
    )

    plt.title(title)
    plt.xlabel(xlabel)
    plt.ylabel(ylabel)

    plt.xticks(
        ticks=np.arange(len(pivot_table.columns)),
        labels=pivot_table.columns,
        rotation=30,
        ha="right"
    )

    plt.yticks(
        ticks=np.arange(len(pivot_table.index)),
        labels=pivot_table.index
    )

    plt.colorbar(image, label=colorbar_label)

    if annotate:
        for row_index in range(pivot_table.shape[0]):
            for column_index in range(pivot_table.shape[1]):
                value = pivot_table.iloc[row_index, column_index]

                if pd.notna(value):
                    plt.text(
                        column_index,
                        row_index,
                        f"{value:.3f}",
                        ha="center",
                        va="center",
                        fontsize=7
                    )

    plt.tight_layout()


def safe_divide(numerator: float, denominator: float) -> float:
    if denominator == 0:
        return 0.0

    return numerator / denominator

def safe_log(value: float, base: float = math.e) -> float:
    if value <= 0:
        return 0.0

    if base == 2:
        return math.log2(value)

    return math.log(value)

def normalize_probability_vector(vector: np.ndarray) -> np.ndarray:
    total = vector.sum()

    if total == 0:
        return vector

    return vector / total

def count_edges_inside_node_set(node_set: set, neighbors: dict) -> int:
    if len(node_set) < 2:
        return 0

    edge_count_twice = 0

    for node in node_set:
        edge_count_twice += len(neighbors[node].intersection(node_set))

    return edge_count_twice // 2

def count_length_three_paths(source, target, neighbors: dict) -> int:
    length_three_paths = 0
    target_neighbors = neighbors[target]

    for intermediate_node in neighbors[source]:
        length_three_paths += len(neighbors[intermediate_node].intersection(target_neighbors))

    return length_three_paths

def build_node_index(graph: nx.Graph) -> tuple[dict, dict]:
    nodes = list(graph.nodes())
    node_to_index = {node: index for index, node in enumerate(nodes)}
    index_to_node = {index: node for node, index in node_to_index.items()}

    return node_to_index, index_to_node

def build_sparse_adjacency_matrix(
    graph: nx.Graph,
    node_to_index: dict
) -> sparse.csr_matrix:
    rows = []
    columns = []

    for source, target in graph.edges():
        source_index = node_to_index[source]
        target_index = node_to_index[target]

        rows.extend([source_index, target_index])
        columns.extend([target_index, source_index])

    data = np.ones(len(rows), dtype=float)
    number_of_nodes = graph.number_of_nodes()

    adjacency_matrix = sparse.csr_matrix(
        (data, (rows, columns)),
        shape=(number_of_nodes, number_of_nodes),
        dtype=float
    )

    return adjacency_matrix

def build_transition_matrix(
    adjacency_matrix: sparse.csr_matrix,
    degrees_array: np.ndarray
) -> sparse.csr_matrix:
    inverse_degrees = np.zeros_like(degrees_array, dtype=float)

    nonzero_mask = degrees_array > 0
    inverse_degrees[nonzero_mask] = 1.0 / degrees_array[nonzero_mask]

    inverse_degree_matrix = sparse.diags(inverse_degrees)

    return inverse_degree_matrix @ adjacency_matrix

def build_symmetric_flow_matrix(
    adjacency_matrix: sparse.csr_matrix,
    degrees_array: np.ndarray
) -> sparse.csr_matrix:
    inverse_sqrt_degrees = np.zeros_like(degrees_array, dtype=float)

    nonzero_mask = degrees_array > 0
    inverse_sqrt_degrees[nonzero_mask] = 1.0 / np.sqrt(degrees_array[nonzero_mask])

    normalization_matrix = sparse.diags(inverse_sqrt_degrees)

    return normalization_matrix @ adjacency_matrix @ normalization_matrix


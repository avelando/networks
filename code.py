def aggregate_cross_validation_results(
    evaluation_results: pd.DataFrame
) -> pd.DataFrame:
    grouping_columns = ["network", "method", "score_column"]

    ignored_numeric_columns = {
        "fold_id",
    }

    numeric_columns = [
        column
        for column in evaluation_results.select_dtypes(include=[np.number]).columns
        if column not in ignored_numeric_columns
    ]

    aggregated = (
        evaluation_results
        .groupby(grouping_columns)[numeric_columns]
        .agg(["mean", "std"])
        .reset_index()
    )

    aggregated.columns = [
        "_".join(column).strip("_")
        if isinstance(column, tuple)
        else column
        for column in aggregated.columns
    ]

    return aggregated

